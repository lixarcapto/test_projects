

class PersonReference:

    def __init__(self):
        self.codePerson = 0
        self.codeRegion = 0

    def setPersonCode(number):
        self.codePerson = number

    def setRegionCode(number):
        self.codeRegion = number

    def getPersonCode():
        return self.codePerson

    def getRegionCode():
        return self.codeRegion
